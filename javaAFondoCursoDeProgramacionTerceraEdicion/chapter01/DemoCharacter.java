public class DemoCharacter{
    public static void main(String[] args){
        
        System.out.println("Aqui se suma un entero a una cadena: ");
        for(int i=0; i<5; i++){
            System.out.println(i+"A");
        }

        System.out.println();

        System.out.println("Aqui se suma un entero a un caracter: ");
        for(int i=0; i<5; i++){
            System.out.println(i+'A');
        }



    }//end main
}//end DemoCharacter