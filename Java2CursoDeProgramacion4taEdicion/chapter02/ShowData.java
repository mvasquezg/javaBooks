public class ShowData{
    public static void main(String[] args){
        double a=0, b=0;
        int c=0;
        String s;

        a=3.14;
        b=2.71;
        c=2;
        s="Datos";

        System.out.println(s+": ");
        System.out.println("a = "+a);
        System.out.println("b = "+b);
        System.out.println("c = "+c);
    }

}